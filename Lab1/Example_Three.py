# We can write a program that does several things at once.
# For example, let's print off a number.

x = 3

print(x)

# Notice how the computer knows what x is by the time it prints it off?
# That's because on line 4 (where x = 3 is), we assigned x the value of 3.
# From there until the program exits, or we reassign x to a new number,
# the computer will associate x with 3.

x = 1

print(x)

# Notice how it prints 3, then 1?
# That's because we assigned x a new value.